WITH Top_paying_jobs AS (
    SELECT 
        job_postings_fact.job_id,
        job_postings_fact.job_title,
        job_postings_fact.salary_year_avg,
        name as company_name   
from job_postings_fact 
INNER JOIN company_dim on company_dim.company_id = job_postings_fact.company_id
where job_title_short like 'Data Analyst' 
    and salary_year_avg is NOT NULL
    and job_location = 'Anywhere'
    and job_work_from_home = TRUE
ORDER BY salary_year_avg desc
limit 10
)

SELECT 
    Top_paying_jobs.*,
    skills
from skills_dim 
INNER JOIN skills_job_dim on skills_job_dim.skill_id = skills_dim.skill_id
INNER join Top_paying_jobs on Top_paying_jobs.job_id = skills_job_dim.job_id
ORDER BY salary_year_avg DESC;

